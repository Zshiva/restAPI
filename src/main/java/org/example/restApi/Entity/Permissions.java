package org.example.restApi.Entity;

public class Permissions {

    private int id;
    private String name;

    public Permissions() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
